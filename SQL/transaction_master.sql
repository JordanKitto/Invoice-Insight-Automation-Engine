LEFT JOIN DSS.VIM_STG_T101T_VW t ON h.STATUS = t.STATUSID
LEFT JOIN DSS.ps4_ekko_lastchangedatetime_vw e ON e.EBELN = h.EBELN
LEFT JOIN DSS.vim_otx_pf01_t_1reg_ap_vw r ON r.target_projkey = h.DOCID
