h.DOCID,
h.DOCTYPE,
h.INDEX_DATE,
h.CHANGE_DATE,
h.STATUS,
h.BUKRS,
h.BLART,
h.BLDAT,
h.BUDAT,
h.XBLNR,
h.RMWWR,
h.LIFNR,
h.VEND_NAME,
h.VEND_NAME2,
h.EBELN,
h.DUE_DATE,
h.BARCODE,
h.VENDOR_VAT_NO,
h.BELNR_FI,
h.SNAPSHOT_DT,
t.STATUSID,
t.OBJTXT AS STATUS_TEXT,
r.MAIL_S,
r.TARGET_PROJKEY,
r.LAYOUT_ID,
r.TSP_REGISTER,
e.EBELN,
e.LASTCHANGEDATETIME
